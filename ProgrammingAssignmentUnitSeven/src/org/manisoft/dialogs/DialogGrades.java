    /**
     * Set the object displayed by this dialog.
     * @param data The data
     */
    @Override
    public void setData(EnrolledCourse data) {
        super.setData(data);
        if (data == null) {
            return;
        }
        
        JLabel label = new JLabel("Grades");
        GridBagConstraints gridBagConstraints = new java.awt.GridBagConstraints();
        gridBagConstraints.gridx = 0;
        gridBagConstraints.gridy = 3;
        gridBagConstraints.ipadx = 2;
        gridBagConstraints.ipady = 2;
        gridBagConstraints.anchor = java.awt.GridBagConstraints.LINE_START;
        gridBagConstraints.insets = new Insets(2, 10, 2, 10);
        getContentPane().add(label, gridBagConstraints);
        
        for (int i = 0; i < data.getGradesCount(); i++) {
            JTextField txtGrade = new JTextField();
            txtGrades.add(txtGrade);
            gridBagConstraints = new GridBagConstraints();
            gridBagConstraints.gridx = 1 + i % NUM_GRADES_PER_LINE;
            gridBagConstraints.gridy = 3 + i / NUM_GRADES_PER_LINE;
            gridBagConstraints.ipadx = 2;
            gridBagConstraints.ipady = 2;
            gridBagConstraints.anchor = GridBagConstraints.LINE_START;
            gridBagConstraints.fill = GridBagConstraints.HORIZONTAL;
            gridBagConstraints.insets = new Insets(2, 10, 2, 10);
            getContentPane().add(txtGrade, gridBagConstraints);
        }
        int lines = data.getGradesCount() / NUM_GRADES_PER_LINE
                + (data.getGradesCount() % NUM_GRADES_PER_LINE != 0 ? 1 : 0);
        setSize(getWidth(),
                250 + (txtCourse.getHeight() + 4) * lines);
        updateInterface();
    }
    
    /**
     * Update the dialog component from the data variable.
     */
    @Override
    public void updateInterface() {
        txtCourse.setText(data.getCourse().toString().trim());
        txtStudent.setText(data.getStudent().toString().trim());
        if (txtGrades.size() == data.getGradesCount()) {
            for (int i = 0; i < data.getGradesCount(); i++) {
                txtGrades.get(i).setText("" + data.getGrade(i));
            }
        }
    }
    
    /**
     * Update the data variable from the dialog content.
     */
    @Override
    public void updateData() {
        for (int i = 0; i < txtGrades.size(); i++) {
            JTextField txtField = txtGrades.get(i);
            int grade = Integer.parseInt(txtField.getText());
            data.setGrade(i, grade);
        }
    }
    
    /**
     * Verifies if the dialog's components are valid.
     * @return true if the dialog content is valid
     */
    @Override
    public boolean isValidData() {
        for (int i = 0; i < txtGrades.size(); i++) {
            JTextField txtField = txtGrades.get(i);
            boolean error = false;
            try {
                int grade = Integer.parseInt(txtField.getText());
                error = (grade < 0 || grade > 100);
            } catch (NumberFormatException e) {
                error = true;
            }
            if (error) {
                txtField.requestFocus();
                JOptionPane.showMessageDialog(null,
                        "Given grades should be between 0 and 100.",
                        "Invalid data", JOptionPane.OK_OPTION);
                return false;
            }
        }
        return true;
    }

